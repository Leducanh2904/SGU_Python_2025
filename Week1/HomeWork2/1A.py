# 1. Cho x = 8 và y = 2. Cho biết kết quả các lệnh sau:
# a. x + y * 3
# b. (x + y) * 3
# c. x ** y
# d. x % y
# e. x / 12.0
# f. x // 6
x = 8
y = 2
print("x + y * 3 =", x+y*3)
print("(x + y) * 3 =", (x + y) * 3)
print("x ** y =", x ** y)
print("x % y =", x % y)
print("x / 12.0 =", x / 12.0)
print("x // 6 =", x // 6)
